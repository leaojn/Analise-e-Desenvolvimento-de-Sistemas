package cap02;

public class OperadoresAritmeticos {

	
	public static void main(String[] args) {
		int x = 10;
		int y = 3;
		System.out.print("X = " + x + "\n");
		System.out.print("Y = " + x + "\n");
		System.out.print("-X = " + (-x) + "\n");
		System.out.print("X/Y = " + x + "\n");
		System.out.print("Resto de X por Y  = " + (x % y) + "\n");
		System.out.print("Inteiro de X por Y = " + (int)(x / y) + "\n");
		System.out.print("X + 1 = " + (++x) + "\n");
		

	}

}
